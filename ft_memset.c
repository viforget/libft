#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		((UCHAR *)b)[i] = (UCHAR)c;
		i++;
	}
	return (b);
}
